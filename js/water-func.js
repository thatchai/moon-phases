const waterFuncWaterData = (year, month, day) => {
  //
  if (year != 2022) { return false }

  //
  let key1 = `${year}-${month}`
  let key2 = day - 1
  let data = waterFuncWaterDataRaw()

  //
  if (!data[key1]) { return false }
  if (!data[key1][key2]) { return false }

  //
  return data[key1][key2]
}

